#include "../../include/minishell.h"

/*
** Unset builtin command
** Removes environment variables by key
** Returns 0 on success
*/
int	builtin_unset(char **args, t_env **env)
{
	int	i;

	if (!args[1])
		return (0);
	i = 1;
	while (args[i])
	{
		remove_env_node(env, args[i]);
		i++;
	}
	return (0);
}
